            ></v-text-field>
          </v-card-title>
          <v-data-table
            v-model="searchCourses"
            :headers="headers"
            :items="courses"
            :search="searchCourses"
          >
            <template slot="items" slot-scope="props">
              <td>{{ props.item.cid }}</td>
              <td class="text-xs-left">{{ props.item.name }}</td>
              <v-btn v-on:click.native ="addCourse( props.item )">Select</v-btn>
            </template>
            <v-alert slot= 'no-results' :value="true" color="error" icon="warning">
              Your search for "{{ searchCourses }}" found no results.
            </v-alert>
          </v-data-table>
        </v-card>

      </v-flex>
    </v-layout>
  </v-layout>

    <h1>Selected course : {{ this.display }}</h1>
    <h2>Choose day(s)</h2>
      <treeselect
        name="demo"
        :multiple=true
        :clearable=true
        :open-on-click=true
        :clear-on-select=true
        :options="days"
        :limit="5"
        :max-height="200"
        v-model="value"
      />

    <h2>Choose time</h2>
    <treeselect
      name="demo"
      :multiple=false
      :clearable=true
      :open-on-click=true
      :clear-on-select=true
      :options="timeSelect"
      :max-height="200"
      v-model="time"
    />


    <v-flex class="text-xs-center" mt-5>
      <v-btn color="primary" v-on:click="confirm" type="submit">confirm</v-btn>
    </v-flex>

  </v-container>

</template>



<script>
  import {auth, db} from '../firebase'
  import Treeselect from '@riophae/vue-treeselect'
  import '@riophae/vue-treeselect/dist/vue-treeselect.css'
  export default {
    name: 'board',
    components: { Treeselect },
    data () {
      return {
        user: auth.currentUser,
        major: this.$store.state.major,
        display: '',
        cname: '',
        time: '',
        searchCourses: '',
        searchTaken: '',
        selectedCourse: {
          cid: '',
          name: 'not select yet',
          credits: '',
          day: [],
          prec: [],
          prereq: false,
          time: ''
        },
        courses: [],
        headers: [{text: 'CID', sortable: true, value: 'cid'}, {text: 'Name', value: 'Name'}],
        added: false,
        value: null,
        // define options
        days: [{
          id: 'Monday',
          label: 'Monday'
        }, {
          id: 'Tuesday',
          label: 'Tuesday'
        }, {
          id: 'Wednesday',
          label: 'Wednesday'
        }, {
          id: 'Thursday',
          label: 'Thursday'
        }, {
          id: 'Friday',
          label: 'Friday'
        }, {
          id: 'Saturday',
          label: 'Saturday'
        }, {
          id: 'Sunday',
          label: 'Sunday'
        }],
        timeSelect: [{
          id: '2',
          label: 'Two hour',
          children: [{
            id: '8:00 - 10:00',
            label: '8:00 - 10:00'
          }, {
            id: '10:00 - 12:00',
            label: '10:00 - 12:00'
          }, {
            id: '12:00 - 14:00',
            label: '12:00 - 14:00'
          }, {
            id: '14:00 - 16:00',
            label: '14:00 - 16:00'
          }, {
            id: '16:00 - 18:00',
            label: '18:00 - 20:00'
          } ]
        }, {id: '4',
          label: 'Four hour',
          children: [{
            id: '8:00 - 12:00',
            label: '8:00 - 12:00'
          }, {
            id: '12:00 - 16:00',
            label: '16:00 - 20:00'
          }, {
            id: '16:00 - 20:00',
            label: '16:00 - 20:00'
          }]
        }]
      }
    },
    computed: {
      error () {
        return this.$store.state.error
      },
      loading () {
        return this.$store.state.loading
      }
    },
    methods: {
      getMajor () {
        console.log('in get major')
        return db.ref('lecturers/' + auth.currentUser.uid)
          .once('value').then(function (snapshot) {
            console.log(snapshot.val().major.toLowerCase())
            return snapshot.val().major.toLowerCase()
          })
      },
      addCourse (course) {
        console.log(this.prereq.length)
        this.selectedCourse.name = course.name
        this.selectedCourse.cid = course.cid
        this.selectedCourse.credits = course.credits
        this.cname = course.name
        if (typeof course.prereq === 'undefined') {
          // console.log('undefined from if ')
          var found = false
          let a
          for (a = 0; a < this.prereq.length; a++) {
            if (this.prereq[a]['.key'] === course.cid) {
              found = true
              this.selectedCourse.prec = this.prereq[a].condition
              console.log(this.selectedCourse.prec)
            }
          }
          this.selectedCourse.prereq = found
        } else {
          this.selectedCourse.prereq = course.prereq
          this.selectedCourse.prec = course.prec
        }
      },
      confirm () {
        this.selectedCourse.time = this.time
        this.selectedCourse.day = this.value
        console.log(this.selectedCourse)
        // console.log(auth.currentUser.uid)
        db.ref('lecturers/' + auth.currentUser.uid + '/courses').push(
          this.selectedCourse)
      }
    },
